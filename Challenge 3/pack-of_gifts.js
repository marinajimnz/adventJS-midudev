function distributeGifts(packOfGifts, reindeers) {
    let gifts_len = 0;
    let reindeer_len = 0;
    packOfGifts.forEach(gift => {
      return gifts_len += gift_len;
    })
    reindeers.forEach(reindeer => {
      return reindeer_len += (reindeer_len)*2;
    })
    return Math.floor(reindeer_len/gifts_len);
  }